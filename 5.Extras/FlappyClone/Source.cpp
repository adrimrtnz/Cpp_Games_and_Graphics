#include <SFML/Graphics.hpp>

// Window Parameters
const int S_WIDTH = 1024;
const int S_HEIGHT = 768;

sf::Vector2f viewSize(S_WIDTH, S_HEIGHT);
sf::VideoMode vm(viewSize.x, viewSize.y);
sf::RenderWindow window(vm, "FlappyClone", sf::Style::Default);

int main(void)
{
	while (window.isOpen()) {

		window.clear(sf::Color(0,0,128,255));
		window.display();

	}
}